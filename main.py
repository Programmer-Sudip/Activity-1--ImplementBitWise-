# Program to showcase working of different types of bitwise operators

num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

# Using AND operator
print("num1 & num2 = ", num1 & num2)

# Using OR operator
print("\nnum1 | num2 = ", num1 | num2)

# Using NOT operator
print("\n~num1 = ", ~num1)

# Using XOR operator
print("\nnum1 ^ num2 = ", num1 ^ num2)


num1 = int(input("Enter a number: "))
